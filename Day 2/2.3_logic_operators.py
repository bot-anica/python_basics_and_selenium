# ---------- ЛОГИЧЕСКИЕ ОПЕРАЦИИ
print("----- LOGICAL OPERATIONS -----")

# Логические операции
# == - равенство
# not - отрицание
# and - и
# or - или
# is - проверяет ссылаються ли две переменные на один и тот же объект
# is not - проверяет ссылаються ли две переменные на разные объекты

print(True or False)
print(True and False)
print(not True)
print(not False)
print(2 == 2)
print(2 != 2)


# ---------- МАТЕМАТИЧЕСКИЕ И ЛОГИЧЕСКИЕ ОПЕРАЦИИ
print("\n----- MATH & LOGICAL OPERATIONS -----")

# > - больше
# < - меньше
# >= - больше или равно
# <= - меньше или равно

a = 5
b = 2
c = 10

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a >= c)
print(a <= c)
